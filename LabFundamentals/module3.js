console.log("" - 1 + 0); // initial answer is -1 then correct
console.log(true + false); // initial answer is 1 then correct
console.log(!true); // initial answer is false then correct
console.log(6 / "3"); // initial answer is 2 then correct
console.log("2" * "3"); // initial answer is 6 then correct
console.log(4 + 5 + "px"); // initial answer is "9px" then correct
console.log("$" + 4 + 5); // initial answer is "$45" then correct
console.log("4" - 2); // initial answer is 2 then correct
console.log("4px" - 2); // initial answer is NaN then correct
console.log(" -9 " + 5); // initial answer is " -9 5" then correct
console.log(" -9 " - 5); // initial answer is -14 then correct
console.log(null + 1); // initial answer is 1 then correct
console.log(undefined + 1); // initial answer is NaN then correct
console.log(undefined == null); // initial answer is true then correct
console.log(undefined === null); // initial answer is false then correct
console.log(" \t \n" - 2); // initial answer is NaN then correct